#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst && f)
	{
		while (lst)
		{
			f(lst->content);
			lst = lst->next;
		}
	}
}
// lst est le premier element de la liste
// on parcourt la liste en faisant avancer lst
// et on applique la fonction f au content de lst
